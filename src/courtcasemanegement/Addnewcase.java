    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            // Extract data from the form fields
    String caseId = jTextField1.getText().trim();
    String caseDate = jTextField2.getText().trim();
    String caseName = jTextField3.getText().trim();
    String applicableSection = jTextField4.getText().trim(); // Comma-separated sections, e.g. "302, 307"

    // Validate input fields
    if (caseId.isEmpty() || caseDate.isEmpty() || caseName.isEmpty() || applicableSection.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required.");
        return; // Early exit if validation fails
    }

    try {
        // Prepare sections as an array (removes any spaces around commas)
        String[] sectionsArray = applicableSection.split("\\s*,\\s*");

        // Construct the JSON data to be sent
        StringBuilder sectionsJson = new StringBuilder("[");
        for (int i = 0; i < sectionsArray.length; i++) {
            sectionsJson.append("\"").append(sectionsArray[i]).append("\"");
            if (i < sectionsArray.length - 1) {
                sectionsJson.append(",");
            }
        }
        sectionsJson.append("]");

        String caseData = "{"
            + "\"caseId\":\"" + caseId + "\","
            + "\"caseName\":\"" + caseName + "\","
            + "\"applicableSection\":" + sectionsJson.toString() + ","
            + "\"caseDate\":\"" + caseDate + "\""
            + "}";

        // Set up the connection to the Node.js API
        URL url = new URL("http://localhost:3000/v1/add-case"); // Adjust URL if needed
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        conn.setRequestMethod("POST");
        conn.setRequestProperty("Content-Type", "application/json");
        conn.setDoOutput(true);

        // Send the JSON case data
        try (OutputStream os = conn.getOutputStream()) {
            byte[] input = caseData.getBytes(StandardCharsets.UTF_8);
            os.write(input, 0, input.length);
        }

        // Check the response from the server
        int responseCode = conn.getResponseCode();
        if (responseCode == 201) { // Case added successfully
            JOptionPane.showMessageDialog(this, "Case added successfully.");
        } else {
            // Read the error response from the server if the case addition failed
            try (Scanner scanner = new Scanner(conn.getErrorStream(), StandardCharsets.UTF_8.name())) {
                String responseBody = scanner.useDelimiter("\\A").next();
                JOptionPane.showMessageDialog(this, "Error: " + responseBody);
            }
        }
    } catch (Exception e) {
        e.printStackTrace(); // Log the exception for debugging
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String sectionId = jTextField1.getText().trim();
    String priority = jTextField2.getText().trim();
    String sectionName = jTextField3.getText().trim();

    // Validate input fields
    if (sectionId.isEmpty() || priority.isEmpty() || sectionName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required.");
        return; // Early exit if validation fails
    }

    try {
        // Construct the JSON data to be sent
        String sectionData = "{"
            + "\"section\":\"" + sectionId + "\","
            + "\"sectionName\":\"" + sectionName + "\","
            + "\"priority\":\"" + priority + "\""
            + "}";

        // Set up the connection to the Node.js API
        URL url = new URL("http://localhost:3000/v1/add-section"); // Adjust URL if needed
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        conn.setRequestMethod("POST");
        conn.setRequestProperty("Content-Type", "application/json");
        conn.setDoOutput(true);

        // Send the JSON section data
        try (OutputStream os = conn.getOutputStream()) {
            byte[] input = sectionData.getBytes(StandardCharsets.UTF_8);
            os.write(input, 0, input.length);
        }

        // Check the response from the server
        int responseCode = conn.getResponseCode();
        if (responseCode == 201) { // Section added successfully
            JOptionPane.showMessageDialog(this, "Section registered successfully.");
        } else {
            // Read the error response from the server if section addition failed
            try (Scanner scanner = new Scanner(conn.getErrorStream(), StandardCharsets.UTF_8.name())) {
                String responseBody = scanner.useDelimiter("\\A").next();
                JOptionPane.showMessageDialog(this, "Error: " + responseBody);
            }
        }
    } catch (Exception e) {
        e.printStackTrace(); // Log the exception for debugging
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed


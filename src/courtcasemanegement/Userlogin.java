    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword()).trim();
        String userType = "user";
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Blank fields are not allowed");
        } else {
            try {
                // Create the login JSON object
 
                String loginData = "{\"username\":\"" + username + "\", \"password\":\"" + password + "\", \"userType\":\"" + userType + "\"}";

                // Set up the connection to the Node.js API
                URL url = new URL("http://localhost:3000/v1/login"); // Replace with your actual API URL
                HttpURLConnection conn = (HttpURLConnection) url.openConnection();
                conn.setRequestMethod("POST");
                conn.setRequestProperty("Content-Type", "application/json");
                conn.setDoOutput(true);

                // Send the login data
                try (OutputStream os = conn.getOutputStream()) {
                    byte[] input = loginData.getBytes(StandardCharsets.UTF_8);
                    os.write(input, 0, input.length);
                }

                // Read the response
                int responseCode = conn.getResponseCode();
                if (responseCode == 200) { // Success
                    Userdashboard1 m = new Userdashboard1();
                    this.hide();
                    m.setVisible(true);
                    JOptionPane.showMessageDialog(this, "Login successful");
                } else {
                    // Added a Scanner to read and display response from the server if login fails
                    Scanner scanner = new Scanner(conn.getInputStream(), StandardCharsets.UTF_8.name());
                    String responseBody = scanner.useDelimiter("\\A").next();
                    scanner.close();
                    JOptionPane.showMessageDialog(this, "Invalid login: " + responseBody);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

